public class TiendaEnLinea {
    public static void main(String[] args) {
        System.out.println("...... tienda en linea ......");
        String nombreProducto = "arroz";
        double precio = 3500.00;
        int cantidadDisponible = 25;
        boolean disponibe = true;
        System.out.println("nombreProducto = " + nombreProducto);
        System.out.println("precio = $" + precio);
        System.out.println("cantidadDisponible = " + cantidadDisponible);
        System.out.println("disponibe = " + disponibe);
        System.out.println();
        nombreProducto = "pan";
        precio = 2000.00;
        cantidadDisponible = 00;
        disponibe = false;
        System.out.println("nombreProducto = " + nombreProducto);
        System.out.println("precio = $" + precio);
        System.out.println("cantidadDisponible = " + cantidadDisponible);
        System.out.println("disponibe = " + disponibe);
    }
}
